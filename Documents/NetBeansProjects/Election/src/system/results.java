    private void consActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consActionPerformed
        
        String selected = (String)cons.getSelectedItem();

        try{
            PreparedStatement p = con.prepareStatement("select candidate,party,count(candidate) as count from votes where cons = ? group by candidate");
            p.setString(1, selected);
            ResultSet rs = p.executeQuery();
            rs.beforeFirst();
            if(rs.next())
            {
                candidate.setText(rs.getString("candidate"));
                party.setText(rs.getString("party"));
                votes.setText(Integer.toString(rs.getInt("count")));
                
                PreparedStatement p2 = con.prepareStatement("insert into results values(?,?)");
                p2.setInt(1, Integer.parseInt(selected));
                p2.setString(2, party.getText());
                p2.execute();

            }
            
                        

        }
        catch(Exception e){
            System.out.println(e);
    }                                      // TODO add your handling code here:
    }//GEN-LAST:event_consActionPerformed

